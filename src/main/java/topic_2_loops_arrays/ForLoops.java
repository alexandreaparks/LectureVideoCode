package topic_2_loops_arrays;

public class ForLoops {
    public static void main(String[] args) {

        // print numbers 1-5
        // start at 1, count up to and include 5, increase by 1 every iteration
        for (int x = 1 ; x <= 5 ; x++) {
            System.out.println(x);
        }

        System.out.println();

        // 10 down to 0
        // start at 10, count down to 0, decrease by 1 every iteration
        for (int x = 10 ; x >= 0 ; x--) {
            System.out.println(x + "...");
        }
        System.out.println("Blast off!");

        //
    }
}
