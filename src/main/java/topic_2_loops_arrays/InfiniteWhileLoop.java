package topic_2_loops_arrays;

public class InfiniteWhileLoop {
    public static void main(String[] args) {

        // infinite while loop
        // press CTRL-F2 to stop loop or red square

        // no way for the condition to become false - it will run forever
        while (true) {
            System.out.println("This is an infinite while loop!");
        }
    }
}
